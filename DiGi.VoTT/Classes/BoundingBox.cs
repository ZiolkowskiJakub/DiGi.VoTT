namespace DiGi.VoTT.Classes
{
    /// <summary>Represents a rectangular bounding box defined by its position and dimensions.</summary>
    public class BoundingBox
    {
        /// <summary>Gets or sets the left coordinate of the bounding box.</summary>
        public float left { get; set; }
        /// <summary>Gets or sets the top coordinate of the bounding box.</summary>
        public float top { get; set; }
        /// <summary>Gets or sets the width of the bounding box.</summary>
        public float width { get; set; }
        /// <summary>Gets or sets the height of the bounding box.</summary>
        public float height { get; set; }
    }
}
